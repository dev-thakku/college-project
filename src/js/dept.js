const queryString = window.location.search;
const type = new URLSearchParams(queryString).get('type');

if (type && type < deptData.length && type >= 0) {
  document.getElementById('courses').remove();

  const selectedDep = deptData[type];
  const deptTitle = document.querySelector('#dept-title');
  deptTitle.textContent = selectedDep.name;
  if (selectedDep.name.length > 30) deptTitle.classList.add('long-title');
  document.querySelector('#dept-image').src = selectedDep.image;
  constructPage(selectedDep);
} else {
  document.getElementById('content').remove();
  document.querySelector('nav.sec-nav').remove();
}

const navItems = document.querySelectorAll('.tab-link');

navItems.forEach((item) => {
  item.addEventListener('click', handleTabClick);
});

function handleTabClick(e) {
  const tab = e.target.dataset.tabName;
  addActiveClass(e.target);

  const newURLParam = new URLSearchParams(queryString);
  newURLParam.set('tab', tab);

  window.history.pushState({ tab }, tab, `dept.html?${newURLParam.toString()}`);
  constructPage(deptData[type], tab);
}

window.addEventListener('popstate', () => {
  const tab = window.history.state?.tab || 'about';
  constructPage(deptData[type], tab);
});

function constructPage(dept, tab = '') {
  const contentContainer = document.querySelector('#content');
  contentContainer.innerHTML = '';

  if (!tab) tab = new URLSearchParams(queryString).get('tab');

  tab = tab?.toLowerCase() || 'about';

  let title;
  let content;

  const activeTab = document.querySelector(`[data-tab-name=${tab}]`);

  addActiveClass(activeTab);

  switch (tab) {
    case 'facilities':
      title = createTitle('Facilities');
      content = dept.facilities;
      break;
    case 'faculties':
      title = createTitle('Faculties');
      content = getFacultyContent(dept.faculties);
      break;
    case 'vision':
      title = createTitle('Vision and Mission');
      content = dept.vision;
      break;
    case 'about':
      title = createTitle('About');
      content = dept.about;
      break;
    default:
      title = createTitle('About');
      content = dept.about;
  }

  contentContainer.appendChild(title);
  contentContainer.innerHTML += content;
}

function getFacultyContent(faculties) {
  if (!faculties || faculties.length === 0)
    return '<p>No Faculty Data available</p>';
  let container = document.createElement('div');
  container.id = 'staff-container';

  faculties.forEach((faculty) => {
    container.appendChild(createFacultyCard(faculty));
  });

  return convertNodeToString(container);
}

